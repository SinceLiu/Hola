public class RemarkerInfo {

    private double remarkerLat;

    private double remarkerLon;

    private String momentId;

    public String getMomentId() {
        return momentId;
    }

    public void setMomentId(String momentId) {
        this.momentId = momentId;
    }

    public double getRemarkerLat() {
        return remarkerLat;
    }

    public void setRemarkerLat(double remarkerLat) {
        this.remarkerLat = remarkerLat;
    }

    public double getRemarkerLon() {
        return remarkerLon;
    }

    public void setRemarkerLon(double remarkerLon) {
        this.remarkerLon = remarkerLon;
    }

}
